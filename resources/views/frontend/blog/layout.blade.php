									<p>
										<b>В 2012 году ролик «Back to The Start» сети Chipotle стал настоящим триумфатором в Каннах, получив 16 львов и 2 гран-при. А через полгода агентство CAA получило Effie, 4A и Cannes Effectiveness за эффективность стратегии. Я решил разобраться в ней подробнее и посмотреть, чем же эта работа заслужила столь высокие оценки жюри на конкурсах, где оценивается не трогательность ролика, а стратегические решения.</b>
