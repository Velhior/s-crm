На фестивале в Каннах я случайно и буквально на 15 минут забежал послушать какой-то мастер-класс для стратегов. Лектор рассказывал о нескольких стадиях становления стратега и о том, что эти стадии совпадают с теорией Брюса Ли о развитии мастеру Кунг-Фу. 
Прошло 5 лет и мне показалось интересным вернуться к этой теории, чтобы попробовать рассмотреть советы Брюса Ли для молодых стратегов чуть подробнее. </b>
