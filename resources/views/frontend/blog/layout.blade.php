									<div class="col-md-12"><p>В 1979 году Джон Лайнс в своей книге «Креативные стратегии в рекламе» высказал мысль, что «эффективная стратегия не возможна без инстинкта убийства». Мне всегда очень нравилась эта мысль. «Инстинкт убийства» предполагает активные действия, предполагает изменения, в такой стратегии сразу видно за счет кого будет рост и как его добиться. Это не статичное и аморфное «позиционирование» (наличие которого вообще никаким образом не ведет к экономическому успеху). Стратегия с «инстинктом убийства» - это как минимум наличие цели, а понятная цель всегда предполагает совершение каких-то действий для её достижения. Джек Уэлч как-то сказал, что стремление просто удержать своё преимущество - это уже поражение. И в конечном счете всегда выиграет та компания, которая меняется быстрее и агрессивнее, пожирая при этом своих конкурентов. Уэлч показал это на примере GE, которая за 20 лет его руководства увеличилась в 4 раза (будучи при этом лидером рынка) за счет активных «наступательных» действий и постоянного внутреннего изменения. Сегодня эту же мысль высказывает Дэйв Тротт во всех своих выступлениях и недавно написанной книге: Само собой, это не единственный принцип построения эффективной стратегии. И применим он не всегда и не везде. Но в мире рекламы есть масса примеров реальной эффективности именно такого подхода. Вспомните хотя бы Avis, Pepsi или тот же&hellip;</p></div>								</div>
